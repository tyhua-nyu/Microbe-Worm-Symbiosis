function dydt = osc(t,y)
    T1=279.65;
    T2=273.15+25;
    phi=exp((-6900/8.314)*(1/T1-1/T2));
    dydt = zeros(5,1);
    dydt(1)= phi*0.92*0.006*y(3)/(1+y(3)+(y(3)^2)/49)-y(5)*y(1)/(0.00005+y(1))*y(2)/(0.0004+y(2));
    dydt(2)= -1.45*y(5)*y(1)/(0.00005+y(1))*y(2)/(0.0004+y(2));
    dydt(3)= 0.52*y(5)*y(1)/(0.00005+y(1))*y(2)/(0.0004+y(2))-phi*0.92*0.006*y(3)/(1+y(3)+(y(3)^2)/49);
    dydt(4)= 192*phi*0.92*0.006*y(3)/(1+y(3)+(y(3)^2)/49)-124*y(5)*y(1)/(0.00005+y(1))*y(2)/(0.0004+y(2));
    dydt(5)=-0.0000117;
end